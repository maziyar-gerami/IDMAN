package parsso.idman.Utils.SMS.KaveNegar.utils;


public class PairValue {
	private String Key;
	private String Value;

	public PairValue(String Key, String Value) {
		this.Key = Key;
		this.Value = Value;
	}

	public String getKey() {
		return Key;
	}

	public String getValue() {
		return Value;
	}


}
